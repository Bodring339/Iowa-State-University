def distanceSpeedTime(speed, time):
    # calculate distance
    distance = speed * time
    return distance

def velocityAccelerationTime(initial_velocity, acceleration, time):
    # calculate final_velocity
    final_velocity = initial_velocity + (acceleration * time)
    return final_velocity